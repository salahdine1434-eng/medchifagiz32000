<?php
/**
 * medical_report_prompt.php
 * ════════════════════════════════════════════════════════════════
 *  هندسة الـ Prompt الخاصة بالتقارير الطبية.
 *  • systemPrompt(): يُعرّف سلوك المساعد وقواعده الصارمة.
 *  • userPrompt():   يبني رسالة المستخدم من بيانات المريض المنسّقة.
 *
 *  لتحسين جودة/أسلوب التقرير مستقبلاً: عدّل النصوص في هذا الملف فقط.
 * ════════════════════════════════════════════════════════════════
 */

class MedicalReportPrompt
{
    /**
     * رسالة النظام — تُحدّد دور النموذج والقواعد التي لا يجوز تجاوزها.
     */
    public static function systemPrompt(): string
    {
        return <<<SYS
أنت مساعد طبي متخصّص في كتابة التقارير الطبية الرسمية باللغة العربية الفصحى.
مهمتك صياغة تقرير طبي احترافي واضح ومنظّم اعتماداً حصرياً على البيانات المُعطاة.

القواعد الإلزامية:
- لا تخترع أي معلومة غير موجودة في البيانات إطلاقاً.
- إذا كانت معلومة مطلوبة غير متوفّرة فاكتب حرفياً: «لا توجد معلومات كافية».
- لا تستنتج أي تشخيص غير مذكور صراحةً في البيانات.
- استعمل لغة طبية رسمية دقيقة ومحايدة، وخالية من المبالغة.
- اكتب بالعربية فقط، ونظّم التقرير بعناوين واضحة مرقّمة.
- لا تقترح أدوية أو جرعات لم يذكرها الطبيب المعالج.
- تجاهل أي رموز أو إدخالات غير مفهومة بدل محاولة تفسيرها.

أنشئ التقرير بالعناوين التالية وبهذا الترتيب بالضبط:
1) معلومات المريض
2) ملخص الحالة
3) الحالة الحالية
4) أهم الملاحظات الطبية
5) نتائج الفحص
6) تفسير نتائج التحاليل (إن وُجدت)
7) تفسير نتائج الأشعة (إن وُجدت)
8) التقييم السريري
9) التوصيات
10) خطة المتابعة

وفي نهاية التقرير أضف السطر التالي حرفياً ودون تغيير:
«تم إنشاء هذا التقرير بواسطة الذكاء الاصطناعي لمساعدة الطبيب، ولا يُعد بديلاً عن التقييم الطبي النهائي للطبيب.»
SYS;
    }

    /**
     * بناء رسالة المستخدم من بيانات المريض المنسّقة + ملاحظات الطبيب الاختيارية.
     *
     * @param array  $data        مصفوفة [ التسمية العربية => القيمة ] (تُحذف الفارغة).
     * @param string $doctorNotes ملاحظات إضافية من الطبيب (اختياري).
     */
    public static function userPrompt(array $data, string $doctorNotes = ''): string
    {
        $lines   = [];
        $lines[] = 'فيما يلي بيانات المريض المتوفّرة في قاعدة البيانات. اكتب التقرير الطبي اعتماداً عليها فقط:';
        $lines[] = '';

        foreach ($data as $label => $value) {
            $value = self::clean($value);
            if ($value !== '') {
                $lines[] = "- {$label}: {$value}";
            }
        }

        $notes = self::clean($doctorNotes);
        if ($notes !== '') {
            $lines[] = '';
            $lines[] = 'ملاحظات إضافية من الطبيب المعالج (اعتمدها كمعلومة موثوقة):';
            $lines[] = $notes;
        }

        $lines[] = '';
        $lines[] = 'التزم بالقواعد أعلاه وأنشئ التقرير الآن.';

        return implode("\n", $lines);
    }

    /**
     * تنظيف خفيف لقيمة حقل: يحذف القيم الزائفة ويوحّد المصفوفات.
     */
    private static function clean($v): string
    {
        if ($v === null) return '';
        $v = is_array($v) ? implode(' / ', array_filter($v)) : (string) $v;
        $v = trim($v);

        $low = mb_strtolower($v);
        if (in_array($low, ['', 'no', 'none', 'null', '-', '—', 'n/a'], true)) {
            return '';
        }
        return $v;
    }
}
